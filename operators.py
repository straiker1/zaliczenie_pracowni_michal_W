v1 = float(input("First value:"))
v2 = float(input("Second value:"))

print(v1, "+", v2, "=", v1+v2)
print(v1, "-", v2, "=", v1-v2)
print(v1, "*", v2, "=", v1*v2)
print(v1, "**", v2, "=", v1**v2)
if v2 != 0:
    print(v1, "/", v2, "=", v1/v2)
    print(v1, "%", v2, "=", v1%v2)
else:
    print("Second value is 0!")

